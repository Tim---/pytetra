
class CRC(object):
    def __init__(self, poly):
        self.poly = poly

    def compute(self, bits):
        crc = [1] * len(self.poly)
        for i in range(len(bits)):
            b = bits[i]

            msb = crc[0] ^ b
            crc = crc[1:] + [0]
            if(msb):
                crc = [c ^ p for c, p in zip(crc, self.poly)]

        return [c ^ p for p, c in zip([1] * len(self.poly), crc)]


